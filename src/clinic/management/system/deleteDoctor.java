    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String doctorId = x.getText().trim();
    
    if (doctorId.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter a Doctor ID!");
        return;
    }

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/clinic", "root", "");
        String sql = "DELETE FROM doctor_record WHERE ID = ?";
        PreparedStatement ptst = conn.prepareStatement(sql);
        ptst.setInt(1, Integer.parseInt(doctorId));
        
        int rowsAffected = ptst.executeUpdate();
        
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Doctor has been fired!");
        } else {
            JOptionPane.showMessageDialog(null, "No doctor found with the given ID.");
        }
        
        conn.close();
        x.setText("");  
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_jButton4ActionPerformed

